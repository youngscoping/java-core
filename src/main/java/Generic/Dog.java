package Generic;

public class Dog<T extends Number> {
    private String name;
    private T age;

    public Dog(T age, String name) {
        this.age = age;
        this.name = name;
    }

    @Override
    public String toString() {
        return "Dog{" +
                "age=" + age +
                ", name='" + name + '\'' +
                '}';
    }
}
